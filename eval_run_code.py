# Use a structured output to ensure LLM returns a score.  Also returns a comment to provide explanation of score.
class CodeRun(BaseModel):
    """CLass to define the schema for correctness evaluation."""
    success: bool  = Field(description="A bool of the status of the code")
    comment: str = Field(description="An explanation why the code failed")

# Schema for LLM-generated reference output
class ReferenceAnalysis(BaseModel):
    """Schema for LLM-generated reference analysis"""
    analysis: str
    anomaly: Dict[str, Any]
    stats: Dict[str, Any]




# Function to execute code (similar to main.py)
def execute_code_safe(code: str) -> dict:
    """Execute Python code safely and return results."""
    try:
        env = {
            "__builtins__": {
                "__import__": builtins.__import__,
                "print": print,
                "range": range,
            },
            "pd": pd,
            "np": np,
            "datetime": datetime,
        }
        exec(code, env, env)
        if "RESULT" not in env:
            return {"error": "RESULT variable not found in code", "success": False}
        return {"result": env["RESULT"], "success": True}
    except Exception as e:
        return {"error": str(e), "success": False}

