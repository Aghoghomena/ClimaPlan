evaluator_prompt = """
You are evaluating Python code execution for weather data analysis.

The generated code to evaluate:
{generated_code}

The code's execution output:
{code_output}

Your own reference analysis (generated by analyzing the data):
{reference_analysis}

The original prompt:
{prompt}

Instructions:
1. Compare the code's output with your own reference analysis
2. Provide a score from 0 to 100, where 100 means the outputs are the same or very close
3. Explain the score and any differences found

Return whether the code execution was successful (True/False) and your evaluation comment.
"""
evaluator_prompt2 = "You are an expert data analyst specializing in weather data analysis."
@traceable(name="evaluate_code_quality")
def evaluate_code_quality(inputs: dict, outputs: dict, reference_outputs: dict ) -> dict:
    code = outputs.get("generated_code", "")
    hist_file_pat = outputs.get("hist_file_path", None)
    theprompt= inputs.get("prompt")

    #execute the code
    execution_result = execute_code_safe(code)
    if execution_result["success"]:
        execution_result_str = json.dumps(execution_result.get("result", {}), indent=2)
        execution_status = "Success"
    else:
        execution_result_str = execution_result.get("error", "Unknown error")
        execution_status = "Failed"

   #generate the reference data
    reference_llm = llm.with_structured_output(ReferenceAnalysis)
    reference_messages = [
        SystemMessage(content="You are an expert data analyst specializing in weather data analysis."),
        HumanMessage(content=reference_generation_prompt.format(
            path=hist_file_pat,
            prompt=theprompt
        ))
    ]
    
    try:
        reference_analysis = reference_llm.invoke(reference_messages)
        reference_analysis_str = json.dumps(reference_analysis.model_dump(), indent=2)
    except Exception as e:
        reference_analysis_str = f"Error generating reference: {e}"

    #evaluation part

    structured_llm = llm.with_structured_output(CodeRun)
    
    messages = [
            SystemMessage(content=evaluator_prompt2),
            HumanMessage(content=evaluator_prompt.format(prompt=theprompt,
                generated_code=code,
                code_output=json.dumps(execution_result, indent=2) if isinstance(execution_result, dict) else str(execution_result),
                reference_analysis=reference_analysis_str,
            ))
    ]
        
    # extract score and comment from response
    result = structured_llm.invoke(messages)
    print(f"run_status: {result.success}, Comment: {result.comment}")
    # Return ALL fields from the schema
    return {
        "run_status": result.success,
        "comment": result.comment
    }



